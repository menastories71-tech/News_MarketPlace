// Function to transform the provided data to PressRelease model format
function transformPressReleaseData(item) {
  // Extract boolean flags from one_field_for array
  const flags = item.one_field_for || [];
  const best_seller = flags.includes('best seller');
  const newly_added = flags.includes('newly added');
  const exclusive = flags.includes('exclusive');
  const best_price_guarantee = flags.includes('best price guarantee');
  const vaas_choice = flags.includes('VaaS Choice');

  // Handle Google optimization fields
  let google_search_optimised_publications = null;
  if (item.Google_Search_Optimised === 'Guaranteed') {
    // For guaranteed, we need a number - let's use a default or calculate based on distribution
    google_search_optimised_publications = Math.floor(item.Distribution_to_No_of_Media_Websites / 10);
  }

  let google_news_index_publications = null;
  if (item.Google_News_Index === 'Guaranteed') {
    google_news_index_publications = Math.floor(item.Guaranteed_No_of_Media_Placements / 2);
  }

  // Handle word_limit - convert to integer, take first number if range
  let word_limit = item.Word_Limit;
  if (typeof word_limit === 'string') {
    // Extract first number from range like "400-1200"
    const match = word_limit.match(/\d+/);
    word_limit = match ? parseInt(match[0], 10) : 600; // default to 600 if no number found
  } else if (typeof word_limit === 'number') {
    word_limit = word_limit;
  } else {
    word_limit = 600; // default
  }

  return {
    name: item.Press_Release_Name,
    region: Array.isArray(item.Region) ? item.Region[0] : item.Region, // Take first region
    niche: Array.isArray(item.Niche) ? item.Niche[0] : item.Niche, // Take first niche
    distribution_media_websites: item.Distribution_to_No_of_Media_Websites,
    guaranteed_media_placements: item.Guaranteed_No_of_Media_Placements,
    end_client_media_details: item.End_Client_Media_Details_in_Press_Release,
    middlemen_contact_details: item.Middlemen_or_PR_Agency_Contact_Details_in_Press_Release,
    google_search_optimised_status: item.Google_Search_Optimised,
    google_search_optimised_publications: google_search_optimised_publications,
    google_news_index_status: item.Google_News_Index,
    google_news_index_publications: google_news_index_publications,
    images_allowed: item.No_of_Images_allowed,
    word_limit: word_limit,
    package_options: item.Press_Release_Package_Options,
    price: item.Price,
    turnaround_time: item.Turnaround_time_in_days.toString(),
    customer_info_needed: Array.isArray(item.Information_and_Documents_needed_from_customers)
      ? item.Information_and_Documents_needed_from_customers.join(', ')
      : item.Information_and_Documents_needed_from_customers,
    description: item.One_area_for_description,
    image_logo: item.One_image_or_logo_section,
    best_seller: best_seller,
    newly_added: newly_added,
    exclusive: exclusive,
    best_price_guarantee: best_price_guarantee,
    vaas_choice: vaas_choice,
    content_writing_assistance: item.Content_writing_assistance.includes('Required'),
    submitted_by_admin: 1, // Assuming admin submission
    is_active: true
  };
}


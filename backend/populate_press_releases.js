class PressReleasePopulator {
  async populatePressReleases() {
    try {
      console.log('Starting press release population...');

      let successCount = 0;
      let errorCount = 0;

      for (const item of PRESS_RELEASE_DATA) {
        try {
          // Transform the data to match PressRelease model
          const pressReleaseData = transformPressReleaseData(item);

          await this.savePressRelease(pressReleaseData);
          successCount++;
          console.log(`Saved press release: ${pressReleaseData.name}`);
        } catch (error) {
          console.error(`Error processing press release "${item.Press_Release_Name}":`, error.message);
          errorCount++;
        }
      }

      console.log(`Press release population complete. Success: ${successCount}, Errors: ${errorCount}`);

    } catch (error) {
      console.error('Error in populatePressReleases:', error);
    }
  }

  async savePressRelease(pressReleaseData) {
    // Check if press release already exists
    const existing = await this.findPressReleaseByName(pressReleaseData.name);
    if (existing) {
      console.log(`Press release ${pressReleaseData.name} already exists, skipping...`);
      return;
    }

    await PressRelease.create(pressReleaseData);
  }

  async findPressReleaseByName(name) {
    try {
      const sql = 'SELECT * FROM press_releases WHERE name = $1';
      const result = await require('./src/config/database').query(sql, [name]);
      return result.rows[0] || null;
    } catch (error) {
      console.error('Error checking existing press release:', error);
      return null;
    }
  }
}

// Run the populator
if (require.main === module) {
  const populator = new PressReleasePopulator();
  populator.populatePressReleases()
    .then(() => {
      console.log('Press release population script completed successfully');
      process.exit(0);
    })
    .catch((error) => {
      console.error('Press release population script failed:', error);
      process.exit(1);
    });
}

